#include<iostream>
#include<iomanip>

using namespace std;

int main()
{
	cout<<setprecision(4)<<3.14159<<endl;
	cout<<setw(30)<<'a'<<endl;
	cout<<setw(30)<<setfill('%')<<3.14159<<endl; //上面设置的精度在这里仍有效
	cout<<setfill(' ')<<3.14159<<"$"<<endl; //上面设置的宽度在这里已失效
	cout<<setw(30)<<setiosflags(ios::right)<<3.14159<<endl;//填充已失效
	cout<<setw(30)<<3.14159<<endl; //上面设置的对齐标志仍失效
	return 0;
}
/*	头文件为 iomanip
setprecision(n)		设置小数点后保留 n-1 位
setw(m)				设置总域宽为m (包含小数点)
*/
